		for key, value in pairs( objects ) do
			setElementDimension( value, getElementDimension( getLocalPlayer( ) ) )
		end
	elseif getElementDimension( getLocalPlayer( ) ) == 0 and getElementDimension( objects[1] ) ~= 0 then
		for key, value in pairs( objects ) do
			setElementDimension( value, 0 )
		end
	end
end
