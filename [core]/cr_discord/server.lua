WebhookList = {}
WebhookDebug = false

WebhookClass = setmetatable({
    constructor = function(self, args)
        self.username = Webhooks[args].username
        self.link = Webhooks[args].link
        self.avatar = Webhooks[args].avatar

        if WebhookDebug then
            outputConsole("[Webhook] Created channel '" .. args .. "'")
        end

        return self
    end,

    send = function(self, message)
        local sendOptions = {
            connectionAttempts = 3,
            connectTimeout = 5000,
            formFields = {
                content = message:gsub("#%x%x%x%x%x%x", ""),
                username = self.username,
                avatar_url = self.avatar,
            }
        }

        fetchRemote(self.link, sendOptions, function(responseData)
            if WebhookDebug then
                outputConsole("[Webhook] " .. responseData)
            end
        end)
    end
}, {
    __call = function(cls, ...)
        local self = {}
        setmetatable(self, { __index = cls })
        self:constructor(...)
        return self
    end
})

addEventHandler("onResourceStart", resourceRoot, function()
    for name, data in pairs(Webhooks) do
        WebhookList[name] = WebhookClass(name)
    end
end)

function sendMessage(channel, message)
    if WebhookList[channel] then
        WebhookList[channel]:send(message)
        if WebhookDebug then
            outputConsole("[Webhook] Send message '" .. message .. "' from '" .. channel .. "' channel.")
        end
    else
        outputConsole("[Webhook] Couldn't find the Discord Webhook Channel.")
    end
end